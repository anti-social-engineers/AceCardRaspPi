CARD_KEY_A = [0x6B, 0x3D, 0x73, 0x34, 0x4C, 0x29]
CARD_KEY_B = [0x75, 0x42, 0x64, 0x35, 0x5f, 0x5d]

CARD_KEY = [0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF]

def getkey():
    choice = int(input("which key? a=1/b=2/d=>2(default)"))
    if choice == 1:
        return CARD_KEY_A
    elif choice == 2:
        return CARD_KEY_B
    else:
        return CARD_KEY



